     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $ticket = Ticket::with('bus')->findOrFail($request->input('ticket'));

        $bookings = Booking::where(['ticket_id' => $ticket->id])->get('seat_no');

        return view('frontend.booking.create', compact('ticket', 'bookings'));
        
    }

    /**
     * Store a newly created resource in storage.
